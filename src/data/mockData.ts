export interface CropBatch {
  id: string;
  farmerName: string;
  farmerId: string;
  cropName: string;
  cropCategory: CropCategory;
  plantingDate: string;
  location: string;
  state: string;
  district: string;
  status: "planted" | "growing" | "harvested" | "delivered";
  progress: number;
  imageUrl?: string;
  harvestDate?: string;
  estimatedYield: number;
  actualYield?: number;
  unit: string;
  pricePerUnit: number;
  paymentStatus: "pending" | "advance_paid" | "completed";
  qualityGrade?: "A+" | "A" | "B+" | "B" | "C";
  certifications?: string[];
  soilType?: string;
  irrigationType?: string;
  fertilizersUsed?: string[];
  pesticidesUsed?: string[];
  blockchainHash?: string;
}

export interface Farmer {
  id: string;
  name: string;
  location: string;
  state: string;
  district: string;
  totalCrops: number;
  rating: number;
  verified: boolean;
  farmSize: number;
  farmSizeUnit: "acres" | "hectares";
  phone: string;
  email?: string;
  bankAccount?: string;
  govtId: string;
  specializations: CropCategory[];
  organicCertified: boolean;
  joinedDate: string;
  totalEarnings: number;
  loansAvailed: number;
  loansPaid: number;
}

export interface Buyer {
  id: string;
  name: string;
  company: string;
  companyType: "retailer" | "wholesaler" | "processor" | "exporter" | "food_service";
  location: string;
  state: string;
  rating: number;
  verified: boolean;
  phone: string;
  email: string;
  gstNumber: string;
  totalPurchases: number;
  preferredCrops: string[];
  joinedDate: string;
}

export type CropCategory = "cereals" | "pulses" | "oilseeds" | "vegetables" | "fruits" | "spices" | "cash_crops" | "fiber_crops";

export interface Transaction {
  id: string;
  cropBatchId: string;
  farmerId: string;
  buyerId: string;
  amount: number;
  date: string;
  type: "advance" | "final_payment" | "loan";
  status: "pending" | "completed" | "failed";
  blockchainTxHash: string;
}

export interface LoanRequest {
  id: string;
  farmerId: string;
  farmerName: string;
  amount: number;
  purpose: string;
  cropBatchId?: string;
  status: "pending" | "approved" | "disbursed" | "rejected";
  interestRate: number;
  tenure: number;
  appliedDate: string;
  approvedDate?: string;
}

export interface WeatherData {
  location: string;
  state: string;
  temperature: number;
  humidity: number;
  rainfall: number;
  condition: "sunny" | "cloudy" | "rainy" | "stormy";
  forecast: string;
}

export interface MarketPrice {
  cropName: string;
  category: CropCategory;
  currentPrice: number;
  yesterdayPrice: number;
  weeklyChange: number;
  monthlyChange: number;
  unit: string;
  market: string;
  state: string;
  lastUpdated: string;
}

